                <h3>The Road to Dubai: Expert Consultation on Thematic Programs of COP28, 2023</h3>
                <p>Climate Change is the prime concern for the adverse impact and long-term adaptations need to be integrated nowadays in different countries, including Bangladesh. Developed nations emit more greenhouse gases that harm the environment, accelerating negative impacts on developing and least developed countries. Understanding the context and addressing the emergence in our national goals to combat climate change and our overall challenges, the event “The Road to Dubai: Expert Consultation on Thematic Programs of COP28” was organized on November 15, 2023, at a Hotel in Dhaka. This Expert Consultation was organized in collaboration with esteemed partner organizations including Nature Conservation Management (NACOM), Center For Climate Change and Environmental Research (C3ER), UNDP Bangladesh, Action Aid Bangladesh, ECO NETWORK, Youth Net for Climate Justice, Change Initiative, Center for Disability in Development (CDD), Foundation, Prokriti O Jibon Foundation, Local Environment Development and Agricultural Research Society (LEDARS), Center for Atmospheric Pollution Studies, An Organization for Socio-Economic Development (AOSED), CODEC, International Center for Climate Change and Development (ICCCAD). </p>

                <p>
                  The event profoundly focused on the core thematic issues regarding Adaptation, Climate Finances, Loss and Damage, Technology Transfer, Capacity Building, Mitigation, Accountability, Nature and Land Use, Women, Youth, Disability and Indigenous Community with principles and processes for the upcoming COP 28. Honourable Minister Mr. Md. Shahab Uddin (MP), Ministry of Environment, Forest and Climate Change graced the event as the chief guest and Mr. Stephen Liller, UNDP Resident Representative Bangladesh joined as the special guest. </p>
                <p>
                  The meeting was chaired by Dr. Qazi Kholiquzzaman Ahmed, Economist and Climate Change Expert and Chairman, Dhaka School of The speakers were: Mr. Zakir Hussain Khan, Chief Executive, Change Initiative; Dr. M Asaduzzaman, former Director Research and Professional Fellow, BIDS; Dr. Hasib Irfanullah, Visiting Research Fellow, CSD, ULAB; Mr. M Hafizul Islam Khan, Executive Director, CCJ-B; Ms. Farah Kabir, Country Director, ActionAid Bangladesh; Dr. Md Iqbal Kabir, Professor, CCHPU, Ministry of Health and Family Welfare; Mr. Arif M Faisal, Program Specialist, Nature, Climate and Energy, UNDP Bangladesh; Mr. Shamim Arfeen, Executive Director, AOSED; Dr. AKM Saiful Islam, Professor, BUET; Mr. Sohanur Rahman, Executive Coordinator, YouthNet for Climate Justice; Dr. Fazle Rabbi Siddique Ahmed, Deputy Managing Director, PKSF; Professor Kamruzzaman Ahmed Majumder, Director, CAPS, Stamford University; Mr. Mohan Kumar Mondal, Executive Director, LEDARS, Syed Sabbir Hasan, Advocacy and Documentation Officer, CDD; and Mr. Mrinal Kanti Tripura, Director, Malaya Foundation and Observer, UNFCCC-COP. </p>
                <p>Prof. Dr. Ainun Nishat, Professor Emeritus and Advisor, C3ER, BRAC University facilitated the open discussion and emphasized the need to be proactive in safeguarding our participation and interests in the upcoming COP28 global negotiations. </p>
