                    <p>Mr. Shamim Arfeen, Executive Director of AOSED, began his speech by defending the importance of education in addressing climate change. He highlighted the obligation of Annex 1 nations under the Kyoto Protocol to contribute 1% of their tax revenues to education and research in developing nations. However, he noted that these nations have yet to fulfil this obligation. Mr. Arfeen emphasized that since his organization started addressing climate education in 2003, they have provided instruction to 64 upper secondary school students in less than 20 years. </p>
                    <p>He stressed the need for evidence-based qualification in capacity development and argued that teachers, the general public, and political parties should actively participate in climate action. Mr. Arfeen asserted that political leaders, as influential figures in society, should be considerate and take action against climate change. He highlighted the importance of considering various factors in endeavors, citing the example of the impact of taking up agricultural land for constructing a cyclone shelter.</p>

