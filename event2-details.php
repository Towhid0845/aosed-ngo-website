                <p>The speaker urged architects and engineers to be more concerned with seemingly insignificant factors like site selection. He emphasized the necessity of collaboration with international organizations for financing capacity building and education. Mr. Arfeen pointed out that while the government may acquire new equipment, organizations often struggle to utilize it due to a shortage of qualified operators. He called for capacity building across all sectors, focusing on ministries and educational institutions.</p>
                <p>Highlighting the broad scope of education, Mr. Arfeen noted that it includes capacity building, applied education, climate education, technological know-how, and other areas. He suggested that building capacity has become a central focus in climate finance, economics, and geopolitics. The speaker concluded by emphasizing the importance of involving various stakeholders in capacity building to address the multifaceted challenges posed by climate change.</p>
